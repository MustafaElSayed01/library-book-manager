package com.library.model;

/**
 * Enumerates the supported genres for books stored in the library catalog.
 *
 * <p>This enum can be used to keep genre input consistent when books are added
 * through the console menu.</p>
 */
public enum Genre {
    FICTION,
    NON_FICTION,
    SCIENCE,
    HISTORY,
    BIOGRAPHY,
    TECHNOLOGY,
    OTHER;
}
